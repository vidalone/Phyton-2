# **********************************************************************
# Ejemplo de función
# **********************************************************************
def multiplicar(x, y):
    return x * y

print(multiplicar(5,5))


# **********************************************************************
# Ejemplo función lambda
# **********************************************************************
multiplicar = lambda x, y: x * y

print(multiplicar(5, 5))


# **********************************************************************
# Ejemplo lambda dentro de una expresión - Adaptado de PEP 498
# **********************************************************************
print(f'{(lambda x, y: x * y)(5, 5)}')